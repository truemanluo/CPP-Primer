#include <iostream>
#include <string>

using namespace std;

int main()
{
    cout << "string: " << sizeof(string) << endl;
    cout << "int: " << sizeof(int) << endl;
    cout << "short: " << sizeof(short) << endl;
    cout << "long: " << sizeof(long) << endl;
    cout << "unsigned long: " << sizeof(unsigned long) << endl;
    cout << "unsigned int: " << sizeof(unsigned int) << endl;
    return 0;
}